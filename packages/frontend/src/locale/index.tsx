function resolveMessage(locale: AppLocale, key: string): MessageValue | undefined {
  return messages[locale][key] ?? messages.en[key]
}

function normalizeLocale(value?: string | null): AppLocale {
  if (value && value.toLowerCase().startsWith('zh')) {
    return 'zh-CN'
  }
  return 'en'
}

export function detectInitialLocale(): AppLocale {
  if (typeof window === 'undefined') {
    return 'en'
  }
  const stored = window.localStorage.getItem(STORAGE_KEY)
  if (stored === 'en' || stored === 'zh-CN') {
    return stored
  }
  return normalizeLocale(window.navigator.language)
}

export function translate(locale: AppLocale, key: string, params?: MessageParams): string {
  const value = resolveMessage(locale, key)
  if (!value) return key
  if (typeof value === 'function') {
    return value(params ?? {})
  }
  return value.replace(/\{(\w+)\}/g, (_, token) => String(params?.[token] ?? ''))
}

export function getActiveLocale(): AppLocale {
  if (typeof window === 'undefined') {
    return 'en'
  }
  const stored = window.localStorage.getItem(STORAGE_KEY)
  if (stored === 'en' || stored === 'zh-CN') {
    return stored
  }
  return normalizeLocale(window.navigator.language)
}

export function translateApp(key: string, params?: MessageParams, locale: AppLocale = getActiveLocale()) {
  return translate(locale, key, params)
}

export function isDefaultConversationTitle(title?: string | null) {
  if (!title) return true
  const normalized = title.trim()
  return !normalized || DEFAULT_CONVERSATION_TITLE_ALIASES.has(normalized)
}

export function getLocalizedConversationTitle(title: string | null | undefined, t: LocaleTranslate) {
  if (isDefaultConversationTitle(title)) {
    return t('common.newConversation')
  }
  return title?.trim() || t('common.newConversation')
}
